tourist_places = ["beijing", "shanghai", "california", "provence", "melbourne"]
print(tourist_places)
print("\n")

print(sorted(tourist_places))
print(tourist_places)
print("\n")

print(sorted(tourist_places, reverse=True))
print(tourist_places)
print("\n")

tourist_places.reverse()
print(tourist_places)
print("\n")

tourist_places.reverse()
print(tourist_places)
print("\n")

tourist_places.sort()
print(tourist_places)
print("\n")

tourist_places.sort(reverse=True)
print(tourist_places)
print("\n")
